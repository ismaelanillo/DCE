#define F_CPU 16000000UL
// Define CPU frequency as 16 MHz
#include <avr/io.h>
#include <util/delay.h>
#include "SETUP.h"
int main(void) {
	// --------------------------- SETUP ----------------------------
	//---------------------------------------
	// 1. Set pin 5 in port “B” as output pin
	//---------------------------------------
	DDRD |=(1<<DDD3);
	//----------------------------- LOOP ------------------- --------
	while (1) {
		//--------------------------------------------
		// 1.- LED OFF by clearing the pin 5 in port B
		//--------------------------------------------
		PORTD &=~(1<<PORTD3);
		//---------------------------------------
		// 2.- Delay of Nm  milli-seconds
		//---------------------------------------
		_delay_ms(BLINK_TIME);
		//------------------------------------------------
		// 3.- LED ON by setting the pin 5 in port B
		//------------------------------------------------
		PORTD |=(1<<PORTD3);
		//---------------------------------------
		// 4.- Delay of N milli-seconds
		//---------------------------------------
		_delay_ms(BLINK_TIME);
		//---------------------------------------
	}
}

